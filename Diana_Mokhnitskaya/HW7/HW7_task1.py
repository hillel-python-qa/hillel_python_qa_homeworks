def arifmetic_function(number1: int, number2: int, operations: list) -> int:
    for operation in operations:
        if operation: "+"
        result = number1 + number2
        if operation: "-"
        result = number1 - number2
        if operation: "*"
        result = number1 * number2
    else: result = number1 / number2
    return result
